var nolang = require('../nolang');

var app = new nolang.Application();

//----------------------------------------------------------------------------------------------------------------------

app.start(function()
{
    function print(text)
    {
        return text;
    }

    app.spawn(function()
    {
        receive('test', function(data)
        {
            console.log('got data:', data);
        });

        send('test', self, 'Foobar!');
    });
});

//----------------------------------------------------------------------------------------------------------------------

module.exports = {
}; // end exports

